
// A) Write a ‘java’ program to display characters from ‘A’ to ‘Z’.  [15 M] 


public class Java1{
    public static void Display(){
        for(char a = 'A'; a <= 'Z' ; a++){
            System.out.println(a);
        }
    }

    public static void main(String[] args){

        Display();

    }
}